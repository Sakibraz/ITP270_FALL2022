####Viewing and Changing the File System



###Commands
#1. cp
#  a. Copy - The shell command cp is used to copy files or directories.The basic argument structure is cp source destination, where the source is the file/directory to copy to the destination file/directory.

#2. mv
#  a. Move - The shell command mv is used to move a file into a directory. Use mv with the source file as the first argument and the destination directory as the second argument.

#3. rm
#  a. Remove - The shell command rm is used to delete files and directories. The -r flag deletes a directory and all of its files and directories (rm -r).

#4. ls
#  a. List Command & Options - The shell command ls is used to list the contents in a directory. It can be combined with the following command options: [-a] lists all contents including hidden files and directories, [-l] list all contents in long format, [-t] lists all contents by the time they were last modified

#5. Command Options - Options can be used to modify the behavior of shell commands. Shell command options are commonly represented by a single letter preceded by a -. For example, -l, -a, and -d could all be options that follow a shell command.