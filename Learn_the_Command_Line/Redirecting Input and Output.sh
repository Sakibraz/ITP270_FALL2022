####Redirecting Input and Output



###Commands
#1. >>
#  a. Append Redirect shell command - The >> shell command is used to redirect the standard output of the command on the left and append (add) it to the end of the file on the right.

#2. |
#  a. Pipe shell command - The | command is called a pipe. It is used to pipe, or transfer, the standard output from the command on its left into the standard input of the command on its right.

#3. >
#  a. Redirecting Output - The > symbol is used to redirect output by taking the output from the command on the left and passing as input to the file on the right.

#4. cat
#  a. Display - The shell command cat displays the contents of one or more files to the terminal.

#5. grep
#  a. Search - The shell command grep is used to search files for lines that match a pattern and returns the results. Various options can be specified along with the grep command to specify the search.

#6. Case insensitive search - The shell grep command searches files for a particular pattern. The grep command with the -i option can be used to search files for lines that match a pattern, case insensitive, and returns the results.

#7. grep -R
#  a. The shell command grep has a -R option (grep -R) that searches all files in a directory, including its subdirectories, and outputs filenames and lines containing matched results.

#8. Command Line Redirection - On a command line, redirection is the process of using the input/output of a file or command to use it as an input for another file. It is similar but different from pipes, as it allows reading/writing from files instead of only commands. Redirection can be done by using the operators > and >>.